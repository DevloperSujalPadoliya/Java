public class classOne {

    public void main(String[]arg){
        System.out.println("Hello");

        // only send 10 digit int capacity is 10 digit 
        int num = 1234567890; 
        System.out.println(num);

        char num1 = 'S';
        System.out.println(num1);

        // only send 5 digit capacity of 5 digit store 
        short num2 = 11111;
        System.out.println(num2);

         // only send 3 digit capacity of 3 digit store 
        byte num3 = 123;
        System.out.println(num3);

        // this is use to long number and the last added "L"
        long num4 = 1234567891012l;
        System.out.println(num4);
    }
}